from collections import Counter


class Solution:
    @staticmethod
    def gcd(a, b):
        while b:
            a, b = b, a % b
        return a

    def hasGroupsSizeX(self, deck):
        """
        :type deck: List[int]
        :rtype: bool
        """
        counts = Counter(deck)
        values = [v for v in counts.values()]
        maxCount = max(values)
        if maxCount <= 1:
            return False
        if len(values) == 1:
            return True

        # version 1: 遍历2-maxCount，看是否有公因子
        # result = False
        # for i in range(2, maxCount+1):
        #     result = all([n % i == 0 for n in values])
        #     if result:
        #         break
        # return result

        # version 2: GCD
        factor = values[0]
        for i in range(1, len(values)):
            factor = Solution.gcd(factor, values[i])
        return factor >= 2


